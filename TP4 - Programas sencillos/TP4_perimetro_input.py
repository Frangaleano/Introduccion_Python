"""

Cree un script que muestre en pantalla el perímetro de un rectángulo,
leyendo su base y altura desde teclado. perímetro = 2 * (base + altura).

"""

base = int(input("Coloque la medida de la base del rectangulo: "))
altura = int(input("Coloque la medida de la altura del rectangulo: "))
perimetro = 2 * (base + altura)
print("El perimetro es de:", perimetro, "metros cuadrados.")

